public class Response<T> {
    public int code;
    public String msg;
    public T data;
    public String sname;//接口名称

    @Override
    public String toString() {
        return "Response{" +
                "code=" + code +
                ", msg='" + msg + '\'' +
                ", data=" + data +
                ", sname='" + sname + '\'' +
                '}';
    }

    public boolean isSuccess() {
        return code==Constant.OK;
    }
}
