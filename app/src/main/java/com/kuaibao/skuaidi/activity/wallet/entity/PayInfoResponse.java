@JsonIgnoreProperties(ignoreUnknown = true)
public class PayInfoResponse implements Serializable{
    private static final long serialVersionUID = -3884145470924073665L;
    private String order_no;
    private String good_name;
    private String total_amount;
    private String buyer_sp_username;
    private String createtime;
    private String sign;

    public String getOrder_no() {
        return order_no;
    }

    public void setOrder_no(String order_no) {
        this.order_no = order_no;
    }

    public String getGood_name() {
        return good_name;
    }

    public void setGood_name(String good_name) {
        this.good_name = good_name;
    }

    public String getTotal_amount() {
        return total_amount;
    }

    public void setTotal_amount(String total_amount) {
        this.total_amount = total_amount;
    }

    public String getBuyer_sp_username() {
        return buyer_sp_username;
    }

    public void setBuyer_sp_username(String buyer_sp_username) {
        this.buyer_sp_username = buyer_sp_username;
    }

    public String getCreatetime() {
        return createtime;
    }

    public void setCreatetime(String createtime) {
        this.createtime = createtime;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }
}
